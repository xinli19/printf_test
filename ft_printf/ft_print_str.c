#include "libft.h"

int	ft_print_str(char *s)
{
	int	i;
	int sum;

	i = 0;
	sum = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		sum++;
	}
	return (sum);
}
